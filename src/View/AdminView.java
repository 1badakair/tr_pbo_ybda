    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // ambil nilai dari form
        String id = this.lblUId.getText().trim();
        String nama = this.txtUNama.getText().trim();
        String spesialis = this.txtUSpesialis.getText().trim();

// validasi sederhana
        if (id.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pilih dokter yang akan diubah (ID kosong).");
            return;
        }
        if (nama.isEmpty() || spesialis.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama dan Spesialisasi tidak boleh kosong.");
            return;
        }

// panggil controller dengan id juga (lihat bagian controller/DAO)
        DokterController dc = new DokterController();
        boolean status = dc.ubahDokter(id, nama, spesialis);

        if (status) {
            // refresh tabel seperti yang sudah kamu punya
            DefaultTableModel dtm = dc.createTable();
            this.TableTambah.setModel(dtm);
            this.TableUpdate.setModel(dtm);
            this.TableHapus.setModel(dtm);
            dc.tampilkanDokter();
            JOptionPane.showMessageDialog(this, "Berhasil mengubah data Dokter");
        } else {
            JOptionPane.showMessageDialog(this, "Gagal mengubah data Dokter");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

